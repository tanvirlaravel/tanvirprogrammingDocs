            <h3>NoSQL database</h3>
            <div>
              <p>NoSQL (short for "not only SQL") is a type of database that does not use a fixed schema and does not rely on the traditional table-based relational database structure. Instead, NoSQL databases are designed to handle large amounts of unstructured and semi-structured data, and can scale horizontally to support high levels of read and write traffic.</p>
              <p>There are several different types of NoSQL databases, including:</p>
              <ul>
                <li>Document-oriented databases: store data in the form of semi-structured documents, such as JSON or XML, rather than in a structured format like a relational database. Examples: MongoDB, Couchbase, RavenDB.</li>
                <li>Column-family databases: store data in a column-based format rather than a row-based format like in relational databases. Examples: Apache Cassandra, Hbase.</li>
                <li>Key-value databases: store data in the form of key-value pairs. Examples: Redis, Riak.</li>
                <li>Graph databases: store data in the form of nodes and edges, used to represent relationships between data. Examples: Neo4j, ArangoDB.</li>
              </ul>
              <p>NoSQL databases are known for their scalability, high performance and flexibility, they are widely used in big data, real-time web apps, and content management systems.</p>

              <hr>
              <hr>
            </div>

            <h3>Document-oriented databases</h3>
            <div>
              <p>Document-oriented databases are a type of NoSQL database that store data in the form of semi-structured documents, rather than in a structured format like a relational database. These documents can contain fields, arrays, and nested sub-documents, and can vary in structure from one document to another. This allows for more flexibility and ease of use in storing and retrieving data, as well as better scalability and performance.</p>
              <p>A key feature of document-oriented databases is the ability to store and index complex data structures, such as nested arrays and sub-documents. This enables efficient querying and indexing of data based on any field or sub-field within the document, without the need to define a schema upfront.</p>
              <p>Another advantage of document-oriented databases is the ability to handle unstructured and semi-structured data, such as text, images, and videos. This makes them well-suited for use cases like content management systems, social media platforms, and e-commerce websites.</p>
              <p>Examples of popular document-oriented databases include MongoDB, Couchbase, and RavenDB. MongoDB is the most popular document-oriented database, it is a free and open-source cross-platform database that uses JSON-like documents with optional schemas and it is designed to handle large amounts of data and can be used as a database for web applications, as well as for big data processing and analytics.</p>
