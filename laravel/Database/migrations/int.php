<h1>Integer </h1> 

<p>In computer programming and mathematics, an integer is a whole number that does not have any fractional or decimal parts. It represents a discrete quantity or value. Integers can be positive, negative, or zero.</p>

<p>Integers can have different ranges depending on the specific programming language or data type. The range of integers that can be represented and the amount of memory allocated for storing integers may vary. Common integer types include int, short, long, and byte, among others.</p>

<br>
 <p>In MySQL, there are several integer data types available to store different ranges of integer values. </p> 
 <ol>
 	<li>TINYINT: Allows you to store signed or unsigned integers ranging from -128 to 127 (or 0 to 255 for unsigned). It occupies 1 byte of storage space.</li>
 	<li>SMALLINT: Allows you to store signed or unsigned integers ranging from -32,768 to 32,767 (or 0 to 65,535 for unsigned). It occupies 2 bytes of storage space.</li>
 	<li>MEDIUMINT: Allows you to store signed or unsigned integers ranging from -8,388,608 to 8,388,607 (or 0 to 16,777,215 for unsigned). It occupies 3 bytes of storage space.</li>
 	<li>INT (or INTEGER): Allows you to store signed or unsigned integers ranging from -2,147,483,648 to 2,147,483,647 (or 0 to 4,294,967,295 for unsigned). It occupies 4 bytes of storage space.</li>
 	<li>BIGINT: Allows you to store signed or unsigned integers ranging from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807 (or 0 to 18,446,744,073,709,551,615 for unsigned). It occupies 8 bytes of storage space.</li>
 </ol>          

 


