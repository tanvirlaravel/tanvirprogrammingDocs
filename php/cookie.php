    <h3>Types of cookies</h3>
    <div>
        <p>There are two main types of cookies:</p>
        <ol>
            <li>First-party cookies: These cookies are created by the website that the user is visiting.</li>
            <li>Third-party cookies: These cookies are created by a third-party website, such as an advertising network.</li>
        </ol>
    </div>
    <hr>
    <hr>

    <h3>Some examples </h3>
    <div>
        <p>When you log in to a website, a cookie is created to store your login information. This cookie allows you to stay logged in for a certain period of time, so you do not have to enter your username and password every time you visit the website.</p>
        <p>When you add items to your shopping cart on an online store, a cookie is created to store the items in your cart. This cookie allows you to continue shopping without having to add the items to your cart again each time you visit the store.</p>
        <p>When you browse the web, third-party cookies may be used to track your browsing activity. This information can be used to show you targeted advertising or to improve the user experience on websites.</p>
