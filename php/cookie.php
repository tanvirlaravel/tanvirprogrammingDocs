<h2>Cookie</h2>       

    <h3>Intro</h3>
    <div>
    <p>A cookie, also known as an HTTP cookie, web cookie, Internet cookie, browser cookie, or simply cookie, is a <code>small piece of data</code> sent from a website and stored in a user's web browser while the user is browsing that website. Every time the user loads the website, the browser sends the cookie back to the server to notify the website of the user's previous activity.</p>
    <p>A cookie is a small piece of data that is stored on a user's computer or device by a website they visit. It is a way for websites to remember information about the user and their preferences.</p>
    <p></p>
    
   </div>
    <hr>
    <hr>

  
   <h3>Cookies purposes</h3>
   <div>
   <ul>
        <li>Authentication: Cookies can be used to keep track of a user's login status, so that they do not have to enter their username and password every time they visit the website.</li>
        <li>Personalization: Cookies can be used to store a user's preferences, such as their preferred language or the items they have added to their shopping cart.</li>
        <li>Tracking: Cookies can be used to track a user's browsing activity across multiple websites, which can be used for marketing purposes or to improve the user experience.</li>
        <li>Purpose: Cookies are primarily used to enhance the user's browsing experience. They allow websites to remember specific information about the user, such as login credentials, language preferences, shopping cart contents, or personalized settings.</li>
        <li>Storage: Cookies are stored as text files on the user's computer or device. They are associated with a specific domain or website and are sent back to the server with each subsequent request from the user's browser.</li>
        <li>Creation and Retrieval: Cookies are created by the web server and sent to the user's browser as part of the HTTP response. The browser then stores the cookie and includes it in subsequent requests to the same domain. The server can retrieve the cookie's data and use it to personalize the user's experience.</li>
        <li>Types of Cookies: There are two main types of cookies: session cookies and persistent cookies. Session cookies are temporary and are stored only for the duration of the user's browsing session. Persistent cookies have an expiration date and remain on the user's device until that date or until manually deleted.</li>
        <li>Security and Privacy: Cookies are generally considered safe and do not contain viruses or malware. However, they can be used for tracking and profiling user behavior, which has raised privacy concerns. Websites should adhere to privacy regulations and provide transparent information about the use of cookies.</li>
        <li>Cookie Management: Users can control and manage cookies through their browser settings. They can choose to accept or reject cookies, delete existing cookies, or be notified when a website wants to set a cookie. Web developers can also provide options for users to manage their cookie preferences on their websites.</li>
        <li>Limitations: Cookies have limitations, such as size restrictions and being domain-specific. They cannot be accessed by other websites, and different browsers may have different cookie policies or restrictions.</li>
        
   </ul>
    </div>
    <hr>
